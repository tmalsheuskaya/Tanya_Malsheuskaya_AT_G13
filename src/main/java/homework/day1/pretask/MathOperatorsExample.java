package pretask;

public class MathOperatorsExample {
    public static void main(String[] args){
        int a = 8;
        int b = 2;

        System.out.println("8 + 2 = " + (a + b));
        System.out.println("8 - 2 = " + (a - b));
        System.out.println("8 / 2 = " + (a / b));
        System.out.println("8 * 2 = " + (a * b));
    }
}
